package com.nakath.aurudu_nakath;

public class Report {
    String report;
    String Userip;

    public Report(String report, String userip) {
        this.report = report;
        Userip = userip;
    }

    public String getReport() {
        return report;
    }

    public String getUserip() {
        return Userip;
    }
}
